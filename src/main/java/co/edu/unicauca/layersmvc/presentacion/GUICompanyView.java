private void actualizarTabla() {
    List<Project> proyectos = serviceProject.listProjects();
    DefaultTableModel model = (DefaultTableModel) jTablemisproyectosEditable.getModel();

    
    
    // Limpiar la tabla antes de agregar nuevos datos
    model.setRowCount(0);
    
    // Añadir filas de proyectos
    for (Project proyecto : proyectos) {
        model.addRow(new Object[]{
            proyecto.getId(),
            proyecto.getCompanyNit(),
            proyecto.getNombreEmpresa(),
            proyecto.getNombreProyecto(),
            proyecto.getObjetivos(),
            proyecto.getDescripcion(),
            proyecto.getTiempoMax(),
            proyecto.getPresupuesto()
        });
    }
}


private void limpiarCampos() {
    jTextidPro.setText("");
    jTextNit.setText("");  
    jTextNombreEm.setText("");  
    jTextnombrePro.setText("");  
    jTextObjetivos.setText("");  
    jTextdescripcion.setText("");  
    jTexttiempoMax.setText("");  
    jTextpresupuesto.setText("");  
}

private boolean validarCampos() {
    if ( jTextNit.getText().isEmpty() || jTextNombreEm.getText().isEmpty() ||
        jTextnombrePro.getText().isEmpty() || jTextObjetivos.getText().isEmpty() ||
        jTextdescripcion.getText().isEmpty() || jTexttiempoMax.getText().isEmpty() ||
        jTextpresupuesto.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos deben ser llenados.");
            return false;
    }

    try {
        Integer.parseInt(jTextNit.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "El NIT debe ser un número válido.");
        return false;
    }

    try {
        Double.parseDouble(jTextpresupuesto.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "El presupuesto debe ser un número válido.");
        return false;
    }

    return true;
}



