    private void GuardarProyectoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarProyectoActionPerformed
     if (!validarCampos()) {
        return;
    }
    
    // Obtener los valores de los campos de texto
    int id = Integer.parseInt(jTextidPro.getText());
    int companyNit = Integer.parseInt(jTextNit.getText());
    String nombreEmpresa = jTextNombreEm.getText();
    String nombreProyecto = jTextnombrePro.getText();
    String objetivos = jTextObjetivos.getText();
    String descripcion = jTextdescripcion.getText();
    String tiempoMax = jTexttiempoMax.getText();
    Double presupuesto = Double.parseDouble(jTextpresupuesto.getText());
    
    // Crear el estado del proyecto (puedes ajustarlo según lo necesites)
    ProjectState estado = ProjectState.PROPUESTO;  // Asumiendo que el estado es INICIADO al principio

    // Crear una nueva instancia de Project, pasando todos los parámetros
    Project newProject = new Project(id,companyNit, nombreEmpresa, nombreProyecto, 
                                      objetivos, descripcion, tiempoMax, presupuesto, estado);

    // Usar el servicio para guardar el proyecto
    boolean resultado = serviceProject.saveProject(newProject);
    
    if (resultado) {
        // Mostrar mensaje de éxito
        JOptionPane.showMessageDialog(this, "Proyecto guardado exitosamente.");
    } else {
        // Mostrar mensaje de error
        JOptionPane.showMessageDialog(this, "Error al guardar el proyecto.");
    }

    // Limpiar los campos de texto
    limpiarCampos();

    // Actualizar la tabla
    actualizarTabla();
   
    }//GEN-LAST:event_GuardarProyectoActionPerformed

