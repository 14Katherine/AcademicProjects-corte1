   public static void main(String[] args) {
    // Crear los repositorios necesarios
    IProjectRepository projectRepository = new ProjectRepository();  
    IStudentRepository studentRepository = new StudentRepository();  
    ICompanyRepository companyRepository = new CompanyRepository();  // Crear el repositorio de empresas

    // Crear los servicios necesarios
    ServiceProject serviceProject = new ServiceProject(projectRepository, studentRepository);
    ServiceCompany serviceCompany = new ServiceCompany(companyRepository); // Pasar el repositorio de empresas al servicio

    // Crear la vista de la empresa pasando el servicio adecuado
    GUICompanyView view = new GUICompanyView(serviceProject, serviceCompany);
    view.setVisible(true);

    // Actualizar la tabla para mostrar los proyectos registrados
    view.actualizarTabla();  // Llamar al método para llenar la tabla con los proyectos registrados
}


