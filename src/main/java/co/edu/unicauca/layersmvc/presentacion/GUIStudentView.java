private void loadProjects() {
        DefaultTableModel model = (DefaultTableModel) jTableproyectosES.getModel();
        model.setRowCount(0); // Limpiar la tabla

        this.projects = projectRepository.list(); // Obtener lista actualizada

        for (Project p : this.projects) {
            model.addRow(new Object[]{
                p.getId(),          // ✅ Ahora se muestra el ID correcto
                p.getNombreEmpresa(),
                p.getNombreProyecto(),
                p.getObjetivos(),
                p.getDescripcion(),
                p.getEstado()
            });
        }
    }

private void loadMyProjects() {
    DefaultTableModel model = (DefaultTableModel) jTablemisproyectosEs.getModel();
    model.setRowCount(0); // Limpiar la tabla

    ServiceProject serviceProject = new ServiceProject(projectRepository, studentRepository);
    List<Project> myProjects = serviceProject.getProjectsByStudent(currentStudent.getIdentificacion());

    for (Project p : myProjects) {
        model.addRow(new Object[]{
            p.getNombreEmpresa(),
            p.getNombreProyecto(),
            p.getObjetivos(),
            p.getDescripcion(),
            p.getEstado()
        });
    }
}


private void showProjectDetails(Project project) {
        String message = "Objetivos: " + project.getObjetivos() + "\nEstado: " + project.getEstado();
        JOptionPane.showMessageDialog(this, message, "Detalles del Proyecto", JOptionPane.INFORMATION_MESSAGE);
    }

