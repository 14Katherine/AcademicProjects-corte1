    private void jButtonpostularseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonpostularseActionPerformed
     int selectedRow = jTableproyectosES.getSelectedRow();
        if (selectedRow >= 0) {
           
            int projectId = (int) jTableproyectosES.getValueAt(selectedRow, 0);
            int studentId = currentStudent.getIdentificacion();

            ServiceProject serviceProject = new ServiceProject(projectRepository, studentRepository);
            boolean success = serviceProject.postularEstudianteAProyecto(studentId, projectId);

            if (success) {
                JOptionPane.showMessageDialog(this, "Postulación exitosa!", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                loadMyProjects(); 
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo realizar la postulación.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un proyecto para postularse.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButtonpostularseActionPerformed

